    FROM BaiTapKHDL3.CHUYENBAY CB JOIN BaiTapKHDL3.PHANCONG PC 
            ON CB.MACB = PC.MACB
    WHERE CB.XUATPHAT = 'Da Nang'
    GROUP BY CB.MAHANG 
    HAVING COUNT(PC.MACB) < 2;

