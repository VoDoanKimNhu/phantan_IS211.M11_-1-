CREATE TABLE BaiTapKHDL3.HANGHANGKHONG
(
    MAHANG VARCHAR(2) CONSTRAINT PK_HHK PRIMARY KEY,
    TENHANG VARCHAR(40),
    NGTL DATE,
    DUONGBAY NUMBER
)

CREATE TABLE BaiTapKHDL3.CHUYENBAY
(
    MACB VARCHAR(5) CONSTRAINT PK_CB PRIMARY KEY,
    MAHANG VARCHAR(2),
    XUATPHAT VARCHAR(20),
    DIEMDEN VARCHAR(20), 
    BATDAU DATE,
    TGBAY DECIMAL
)

CREATE TABLE BaiTapKHDL3.NHANVIEN
(
    MANV VARCHAR(5) CONSTRAINT PL_NV PRIMARY KEY,
    HOTEN VARCHAR(30),
    GIOITINH VARCHAR(3),
    NGAYSINH DATE,
    NGVL DATE,
    CHUYENMON VARCHAR(20)
)

CREATE TABLE BaiTapKHDL3.PHANCONG
(
    MACB VARCHAR(5),
    MANV VARCHAR(4),
    NHIEMVU VARCHAR(25)
)
