                    WHERE MAKH = proc_lkh;
                    
                    UPDATE cn1.KHACHHANG@dblCN1
                    SET LOAIKH = 'VIP'
                    WHERE MAKH = proc_lkh;
                END IF;
            END IF;
        END LOOP;
    CLOSE cs_KHACHHANG;
