CREATE OR REPLACE FUNCTION FUNC_TEN(HOTEN IN VARCHAR2)
RETURN VARCHAR2 
IS
    TEN VARCHAR2(20);
    MAX_SL NUMBER;
BEGIN

    select length (regexp_replace(HOTEN, '[^ ]+'))  + 1 INTO MAX_SL
    from dual 
    connect by level <= length (regexp_replace(HOTEN, '[^,]+'))  + 1;
    TEN := regexp_substr(HOTEN, '[^ ]+', 1, MAX_SL);
    RETURN TEN;
END;


