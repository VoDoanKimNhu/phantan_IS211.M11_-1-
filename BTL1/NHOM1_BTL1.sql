                select HD1.MAHD,TongTien from cn1.HOADON1@dblCN1 HD1, cn1.HOADON2@dblCN1 HD2
                where HD1.MAHD = HD2.MAHD AND MAKH = proc_makh AND extract(month from NGAYHD)= proc_thang
